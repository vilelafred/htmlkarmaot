							<h3>2 - Trapaças</h3>
							<strong>A) Abuso de Bug</strong><br>
							- É proibido explorar ou abusar de bugs no jogo. <details class="rule-more"><summary>Mais…</summary>

								Qualquer uso intencional de falhas ou bugs no jogo para obter vantagens desleais é estritamente proibido. Isso inclui, mas não se limita a, exploits que afetam a economia do jogo, PvP, progressão de personagens ou qualquer outro aspecto que distorça a experiência de jogo. Jogadores são incentivados a reportar bugs imediatamente à administração. Violações podem resultar em advertências, remoção de progresso e/ou equipamentos ou banimento.
							
</details>

							<br>
							<strong>B) Software Ilegal</strong><br>
							- Não é permitido o uso de software não autorizado ou ilegal. <details class="rule-more"><summary>Mais…</summary>

								O uso de qualquer programa, script ou ferramenta que automatize jogabilidade PvE ou PvP, forneça informações não disponíveis para outros jogadores ou modifique o jogo de qualquer maneira não autorizada é proibido. Isso inclui bots, macros, hacks e qualquer software de terceiros que interfira no funcionamento normal do jogo. Daremos ênfase aos scripts de renovação de magic wall/wild growth instantânea dentro das guerras, lembrando que todas as situações devem ser reportadas e checadas por alguém da equipe. Não aceitaremos imagens e/ou vídeos de terceiros como método de verificação. Violadores podem enfrentar banimento imediato e permanente.
							
</details>
							<br>
							<strong>C) Checagem</strong><br>
							- Coopere com as checagens de integridade realizadas pela administração e entenda seu funcionamento. <details class="rule-more"><summary>Mais…</summary>

								A administração do servidor pode realizar checagens periódicas para garantir que todos os jogadores estejam em conformidade com as regras do jogo. Isso pode incluir verificações de software, análises de comportamento, checagens em nosso banco de dados e outras formas de monitoramento. A não cooperação ou tentativa de obstruir essas checagens será considerada uma violação das regras e pode levar a penalidades severas, incluindo o banimento permanente da conta. Nossa checagem manual apresentará situações não convencionais dentro do jogo, onde você deverá obrigatoriamente interagir com nossa equipe no local para evitar uma punição imediata. As punições serão aplicadas juntamente com provas gravadas do jogador por um membro de nossa equipe, não sendo contestáveis pelo usuário após a decisão final da equipe. Além disso, os banimentos aplicados por checagens externas são feitos através de análises e cruzamento de dados com nosso banco de dados e, por esse motivo, também são situações que não serão contestáveis pelo usuário.
							
</details>
							<br>
							<strong>D) Reports</strong><br>
							- Envie reports de maneira responsável e verdadeira através do jogo ou discord. <details class="rule-more"><summary>Mais…</summary>

								Os jogadores são incentivados a reportar comportamentos suspeitos ou violações de regras à administração do servidor. No entanto, enviar reports falsos ou maliciosos para prejudicar outros jogadores é proibido. O uso responsável do sistema de reports ajuda a manter a integridade e a justiça do jogo. Utilize a ferramenta de report dentro do jogo diretamente no personagem ou em mensagens do chat.
							
</details>
							<br>
							<strong>E) Tolerância</strong><br>
							- O servidor tolera automações apenas em momentos de treinamento, como exercise weapons, cabines de trainer e treinar magic level. <details class="rule-more"><summary>Mais…</summary>

								proibição de automações dentro do servidor é nossa regra mais rígida, sendo assim, aplicaremos as punições nas mesmas sempre que constatado pela nossa equipe. Lembrando que essa é uma verificação manual e dependerá única e exclusivamente do membro da equipe que analisará o caso no momento.
							
</details>
							<br>
							<strong>F) KS, Lure e Trap Red</strong><br>
							- A administração não se envolverá em situações de kill stealing, lure ou trap red, a menos que tais ações comprometam seriamente o bom andamento do jogo. <details class="rule-more"><summary>Mais…</summary>

								Kill stealing (KS), lure (atrair monstros para outros jogadores) e trap red (fazer os jogadores perderem os equipamentos com redskull) são práticas que, embora possam ser frustrantes, fazem parte do ambiente competitivo do jogo. A administração não intervirá em disputas relacionadas a essas práticas, exceto em casos onde elas claramente prejudicam a experiência de jogo de forma significativa e repetitiva. Lembrando que a situação de trap red pode causar punições, uma vez que constatado o multiclient na ação dos jogadores envolvidos. Situações extremas que causam desbalanceamento ou desordem no servidor serão analisadas individualmente, e medidas apropriadas serão tomadas se necessário. Os jogadores são incentivados a resolver tais questões de forma amigável e esportiva.
							
</details>
							<br>
							<strong>G) Automações no OTClient</strong><br>
							- A utilização de funções para atacar/matar monstros no OTClient NÃO é permitida, não será tolerada. <details class="rule-more"><summary>Mais…</summary>

								Caso o jogador esteja utilizando o cliente OTClient com alguma automação e a função do OTClient para atacar monstros e/ou usar magias esteja ativa, ele passará por uma verificação convencional da nossa equipe. Se for constatada, o banimento/exclusão será aplicado ao jogador e sua conta. O servidor conta com a opção "Trainer" integrada, com tudo que é necessário para o treino de forma afk (Auto Spell, Anti Idle, Auto Reconect, Auto Eat Food). Como o nome já diz, o mesmo deve ser utilizado exclusivamente para TREINAR, dentro da área reservada para treinos (Trainers localizados no templo de Thais). Nenhum outro programa auxiliar, macro ou bot externo é autorizado. 
Fora das áreas de treino, somente será autorizado o uso do "Auto Eat Food" e "Anti Idle".
							
</details>
