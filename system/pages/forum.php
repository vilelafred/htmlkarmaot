						<h3>4 - Vendas</h3>
						<br>
						<strong>A) Tolerância</strong><br>
						- A administração possui tolerância zero para transações fraudulentas ou não autorizadas quando divulgadas dentro do jogo. <details class="rule-more"><summary>Mais…</summary>

							Qualquer tentativa de fraudar, enganar ou realizar transações não autorizadas será tratada com extrema seriedade. Isso inclui, mas não se limita a, scam de itens, vendas falsas, manipulação de preços e uso de meios externos para transações financeiras. A administração reserva-se o direito de investigar, reverter e penalizar qualquer atividade suspeita. Penalidades podem incluir advertências severas, remoção de itens ou personagens e suspensão temporária ou permanente da conta. Atente-se que não controlaremos grupos externos que fazem esse tipo de venda e não agiremos em situações onde a negociação foi feita particularmente entre os jogadores. Iremos punir quando houver interferência severa em nosso jogo e/ou houverem anúncios dentro de canais públicos do servidor.
						
</details>
						<br>
						<strong>B) Anúncios</strong><br>
						- Para anunciar um personagem à venda, ele deve estar listado no sistema de Bazaar. <details class="rule-more"><summary>Mais…</summary>

							Qualquer anúncio de venda de personagens deve ser feito exclusivamente de personagens que já estejam no sistema de bazaar. Isso garante que todas as transações sejam seguras, verificáveis e dentro das regras do jogo. Anunciar personagens à venda em nossos chats dentro do jogo ou no Discord é proibido caso esse personagem não se encontre em nossa plataforma do bazaar. Caso o anúncio contenha um personagem fora do bazaar, os jogadores responsáveis poderão ser advertidos ou banidos.
						
</details>
						<br>
						<strong>C) Transações no Market</strong><br>
						- O uso excessivo do market para compra e venda de Karma Coins pode causar instabilidade na conta. A administração não se responsabiliza por falhas causadas por uso abusivo do sistema. <details class="rule-more"><summary>Mais…</summary>

							O sistema de market possui limites técnicos para evitar falhas. Realizar transações em excesso, pode causar erros visuais, instabilidade ou problemas na conta. Não nos responsabilizamos por prejuízos causados por esse uso abusivo, já que o sistema funciona corretamente dentro de padrões normais de uso.
						
</details>
