						<h3>3 - Mensagens &amp; Declarações</h3>
						<strong>A) Ofensivas</strong><br>
						- Não toleramos qualquer tipo de assédio, comentários racistas, sexistas, homofóbicos ou qualquer extremismo dentro do jogo. <details class="rule-more"><summary>Mais…</summary>

							Mensagens ofensivas incluem, mas não se limitam a, ameaças, assédio, comentários racistas, sexistas ou qualquer forma de discurso de ódio. Respeito mútuo é fundamental para a comunidade. Qualquer violação desta regra pode resultar em advertências, silenciamento temporário ou permanente, e em casos graves, banimento da conta.
						
</details>
						<br>
						<strong>B) Dados Pessoais</strong><br>
						- Não compartilhe ou solicite informações pessoais de outros jogadores. <details class="rule-more"><summary>Mais…</summary>

							Divulgar ou solicitar dados pessoais, endereço, número de telefone, informações de contas bancárias ou qualquer outra informação sensível, é estritamente proibido. A segurança e privacidade dos jogadores são prioridades absolutas. Qualquer tentativa de violação pode resultar em ações severas, incluindo banimento.
						
</details>
						<br>
						<strong>C) Publicidade</strong><br>
						- É proibido anunciar outros servidores ou produtos comerciais sem permissão. <details class="rule-more"><summary>Mais…</summary>

							Qualquer forma de publicidade não autorizada, incluindo a promoção de outros jogos, produtos, serviços comerciais, é estritamente proibida. Somente anúncios aprovados pela administração do servidor são permitidos e serão feitos sempre pela staff. Violação desta regra pode resultar em advertências ou banimento.
						
</details>
						<br>
						<strong>D) Spam</strong><br>
						- Não é permitido enviar mensagens repetitivas ou irrelevantes que perturbem a comunicação dentro dos canais públicos do jogo. <details class="rule-more"><summary>Mais…</summary>

							Spam inclui a repetição contínua da mesma mensagem, envio de mensagens irrelevantes nos chats World Chat, Advertising e Help, uso excessivo de caracteres especiais ou qualquer comportamento que interfira na experiência de jogo dos outros jogadores. O objetivo é manter os canais de comunicação claros e úteis para todos. Violações podem resultar em silenciamento temporário ou outras penalidades.
						
</details>
						<br>
						<strong>E) Apoiando Violações de Regra</strong><br>
						- Não apoie, incentive ou participe de ações que violem as regras do servidor. <details class="rule-more"><summary>Mais…</summary>

							Apoiar ou incentivar outros jogadores a violar as regras do servidor, seja através de instruções, encorajamento ou qualquer outra forma de assistência, é proibido. Isso inclui, mas não se limita a, ajudar em exploits, hacks, trapaças ou qualquer comportamento que comprometa a integridade do jogo. Violações desta regra são tratadas com seriedade e podem resultar em banimento imediato.
						
</details>
							<br>
							<strong>H) Multi-Client (MC) em PvP/War</strong><br>
							- Em guerras/PvP, cada jogador pode atuar ativamente com apenas um personagem. <details class="rule-more"><summary>Mais…</summary>

								No servidor é permitido logar até 4 clientes simultaneamente. Entretanto, em situações de PvP/War a participação ativa deve ocorrer somente com um único personagem (seu main).
								"Participar ativamente" inclui: fazer ou invadir traps, empurrar jogadores, sumonar criaturas, curar, atacar, usar magic wall/wild growth e ações equivalentes.
								Deixar um maker parado, sem interferir, é permitido; o problema é quando o MC passa a bloquear, atrapalhar ou criar vantagem injusta na guerra.
								Quem descumprir esta regra receberá IP ban no personagem principal (Main Character) e também no(s) maker(s) envolvido(s).
								Objetivo: manter o PvP/War justo, competitivo e divertido para todos.
							
							</details>
