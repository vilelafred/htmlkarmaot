						<h3>5 - Condutas Dentro dos Canais e No Jogo</h3>
						<strong>A) Help</strong><br>
						- Use o canal de Help exclusivamente para perguntas e respostas relacionadas ao jogo. <details class="rule-more"><summary>Mais…</summary>

							O canal de Help deve ser utilizado para questões, dúvidas e solicitações de ajuda sobre mecânicas do jogo, quests, comandos e outros tópicos diretamente relacionados ao jogo. É proibido usar este canal para discussões irrelevantes, spam ou qualquer outro propósito que não seja ajudar e obter ajuda. Jogadores que enviem mensagens que não seguem estas diretrizes podem ser advertidos ou temporariamente silenciados.
						
</details>
						<br>
						<strong>B) Advertising</strong><br>
						- Anúncios devem ser feitos apenas nos canais apropriados e com moderação. <details class="rule-more"><summary>Mais…</summary>

							Qualquer forma de publicidade para sua live, incluindo a venda de itens, busca de membros para guildas ou anúncios de eventos, deve ser feita nos canais designados para tal finalidade. É proibido fazer anúncios em canais não apropriados, como o Help ou World Chat. Anúncios repetitivos e excessivos também são proibidos. Infrações podem resultar em advertências, silenciamento temporário ou outras penalidades conforme necessário.
						
</details>
						<br>
						<strong>C) World Chat</strong><br>
						- Use o World Chat para discussões gerais, mantendo o respeito e a civilidade. <details class="rule-more"><summary>Mais…</summary>

							O World Chat é destinado a conversas gerais entre jogadores sobre diversos tópicos, incluindo a procura e formação de grupos, desde que respeitosos e apropriados. Discussões ofensivas, discursos de ódio, spam e qualquer comportamento perturbador são proibidos. Todos os jogadores devem se sentir bem-vindos e respeitados neste canal. Violações desta regra podem levar a advertências ou silenciamento temporário.
						
</details>
						<br>
						<strong>D) Cassinos</strong><br>
						- É estritamente proibida a permanência de personagens utilizados para atividades de cassino dentro dos depots das cidades. <details class="rule-more"><summary>Mais…</summary>

							A proibição de personagens de cassino nos depots busca evitar o uso excessivo de anúncios e a promoção de jogos de azar em áreas de grande movimentação. Essa medida tem como objetivo preservar a funcionalidade dos depots como locais de troca e interação social, garantindo uma experiência de jogo mais fluida e justa para todos os jogadores.
						
</details>
