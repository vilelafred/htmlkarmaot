							<h3>1 - Nomes</h3>
							<strong>A) Nome Violando a Regra de Suporte</strong><br>
							- Não crie nomes que imitem ou façam referência ao suporte ou à administração do jogo. <details class="rule-more"><summary>Mais…</summary>

								Nomes que possam ser confundidos com membros do suporte ou da administração, como "GM", "Admin", "Suporte", ou qualquer variação que sugira autoridade dentro do jogo, são estritamente proibidos. Isso inclui nomes que possam enganar outros jogadores, fazendo-os acreditar que o portador do nome tem poderes ou responsabilidades administrativas. Esses nomes serão alterados imediatamente, e os jogadores podem enfrentar penalidades severas, incluindo banimento.
							
</details>
							<br>
							<strong>B) Nome Ofensivo</strong><br>
								- É proibido criar nomes ofensivos ou desrespeitosos. <details class="rule-more"><summary>Mais…</summary>

								Nomes ofensivos incluem, mas não se limitam a, linguagem vulgar, insultos, ameaças, comentários racistas, sexistas ou qualquer forma de discurso de ódio. O objetivo é garantir um ambiente de jogo respeitoso e seguro para todos os jogadores. Nomes que violam esta regra serão alterados pela administração, e os jogadores responsáveis podem enfrentar advertências ou outras penalidades.
							
</details>
							<br>
							<strong>C) Nome Inadequado</strong><br>
							- Nomes devem ser apropriados e condizentes com o ambiente de jogo. <details class="rule-more"><summary>Mais…</summary>

								Nomes inadequados são aqueles que não são apropriados para um ambiente de jogo, incluindo referências explícitas a violência extrema, temas sexuais ou qualquer outro conteúdo impróprio. Também inclui nomes que imitam ou fazem referência a figuras políticas ou celebridades de maneira desrespeitosa. Nomes que não atendem a esses critérios serão modificados pela administração, e os jogadores podem ser advertidos.
							
</details>
