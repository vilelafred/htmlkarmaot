<h3 style="text-align: center;"><strong>Workbench</strong></h3>
<p style="text-align: center;"><em><b>Disenchanting items using the Workbench:</b></em></p>
<p style="text-align: center;"><em>1. Place rare, epic or legendary items in the Workbench.</em></p>
<p style="text-align: center;"><em>2. Use a Smithing Hammer on the Workbench.</em></p>
<p style="text-align: center;"><em>This will DESTROY the item and extract some magic powder.</em></p>
<p style="text-align: center;"><em>1 Magic Powder:   Rare</em></p>
<p style="text-align: center;"><em>3 Magic Powder:   Epic</em></p>
<p style="text-align: center;"><em>5 Magic Powder:   Legendary</em></p>
<p style="text-align: center;"><em><span style="font-size: 12px; font-weight: bold; color: red; text-decoration: none;">Attention: (Smithing Hammers can be purchased from Carl)</span></em></p>
