					<h1 style="color: #5a2800; font-family: Verdana, Tahoma, Helvetica, sans-serif; text-align: center;"><span style="color: #333333;">Forge System</span></h1>
<p>&nbsp;</p>

<p style="text-align: center;"><em><span style="color: #333333;">In Karma, we have an Forge System that adds a greater value to your equipments and weapons.</span></em></p>
<p style="text-align: center;"><em><span style="color: #333333;">Located at the old Knight Arena in Thais.</span></em></p>
