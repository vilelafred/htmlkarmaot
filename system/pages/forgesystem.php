<h3 style="text-align: center;"><strong><span style="color: #333333;">Anvil</span></strong></h3>
<p style="text-align: center;"><em><b>Enchanting items using the Anvil:</b></em></p>
<p style="text-align: center;"><em>1. Place a regular item in the Anvil.</em></p>
<p style="text-align: center;"><em>2. Use a Smithing Hammer on the Anvil.</em></p>
<p style="text-align: center;"><em>This requires 25 Magic Powder and will attempt to enchant the item.</em></p>
<p style="text-align: center;"><em>10 Magic Powder is wasted if your enchantment fails.</em></p>
<p style="text-align: center;">&nbsp;</p>
<p style="text-align: center;"><em><img style="border: 3px solid orange;" src="/images/forge/workbench.gif" width="500" height="226"></em></p>
