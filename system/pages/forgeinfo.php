<div class="note">
  <b>Clarification about forging menu percentages:</b> the values you see during forging (e.g., “10% crit”, “30% protection”) are the <b>rarity/roll chances</b> inside the forge — they do <b>not</b> directly equal your final damage/healing percentage.
</div>

<br>
<b>Where Attributes Work:</b><br>
Attributes only apply if the item is equipped in a valid slot (weapon, armor, legs, boots, helmet, shield, etc.). Some attributes are weapon/shield-specific.

<div class="note">
  <b>About innate item bonuses:</b> some items have native bonuses (e.g., <i>protection all +5%</i>). These innate bonuses stack normally with forging attributes. Since we now have <b>NO CAP</b>, there’s no cap interaction to worry about.
</div>

<br>
<h3>Tips for Forging Efficiently</h3>
<ul>
  <li>Foque em itens high-end: melhor custo/benefício no longo prazo.</li>
  <li><b>Legendary</b> maximiza o potencial (2 atributos por item).</li>
  <li>Em armas, priorize <b>Spell/Elemental Damage</b>, <b>Critical Chance</b>, <b>Magic Level / Skills</b>.</li>
  <li>Em armaduras, priorize <b>proteções</b> e <b>HP/MP</b> conforme sua build.</li>
  <li>Combine peças com atributos complementares para o seu estilo.</li>
</ul>

<br>
<h3>Additional Notes</h3>
<ul>
  <li>Você não pode repetir o <b>mesmo atributo</b> mais de uma vez no <b>mesmo item</b> (máx. 2 atributos por item em Legendary).</li>
  <li>Como agora é <b>NO CAP</b>, todos os valores forjados somam normalmente nos itens equipados.</li>
  <li>Os atributos só contam se aparecerem na descrição do item como <b>[Attribute: +X / +X%]</b>.</li>
</ul>

