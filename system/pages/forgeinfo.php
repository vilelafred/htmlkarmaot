<h2 class="center">How the Forging System Works</h2>

<div style="width:90%; margin:0 auto;">

<div class="banner">feel free to make your own item in your own type</div>

<b>What is Forging?</b><br>
Forging lets you upgrade equipment with powerful random attributes (Max HP, Spell Damage, Magic Level, protections, skills, etc.). Rolls happen with magic powder + forging tools at the Forge Workbench.

<br><br>
<b>How Attributes Work:</b><br>
Each item can receive random attributes based on the rarity you reach:
<ul>
  <li><b>Rare</b>: 1 attribute</li>
  <li><b>Epic</b>: 2 attributes</li>
  <li><b>Legendary</b>: 2 attributes (maximum per item)</li>
</ul>

Attribute types:
<ul>
  <li><b>Static</b>: flat bonuses (e.g., +150 HP)</li>
  <li><b>Percent</b>: scales with your current stat (e.g., +10% Spell Damage)</li>
  <li><b>Damage</b>: adds elemental/physical bonus damage</li>
</ul>

